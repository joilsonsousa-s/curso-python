n1 = 4
n2 = 3

def adicao():
    res = n1 + n2
    print("A soma de: "+str(n1)+" + "+str(n2)+" = "+str(res))

def subtrair():
    res = n1 - n2
    print("A soma de: "+str(n1)+" - "+str(n2)+" = "+str(res))

def multiplicacao():
    res = n1 * n2
    print("A soma de: "+str(n1)+" x "+str(n2)+" = "+str(res))

def somar():
    adicao()
    subtrair()
    multiplicacao()
somar()